#include "MyMath.h"

/*
Add two integers
*/
int Add(int a, int b)
{
	return (a + b);
}

/*
Multiply two integers
*/
int Multiply(int a, int b)
{
	return(a*b);
}

/*
Subtract two values
*/
int Subtract(double x, double y)
{
	return x - y;
}

/*
Divide two values
*/
int Divide(double x, double y)
{
	return x/y;
}

